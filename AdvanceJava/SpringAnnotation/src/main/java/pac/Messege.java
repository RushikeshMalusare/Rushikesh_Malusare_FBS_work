package pac;

public class Messege {

	String nm;
	String cls;
	
	public Messege() {
		
	}
	
	public Messege(String nm, String cls) {
		super();
		this.nm = nm;
		this.cls = cls;
	}

	public String getNm() {
		return nm;
	}

	public void setNm(String nm) {
		this.nm = nm;
	}

	public String getCls() {
		return cls;
	}

	public void setCls(String cls) {
		this.cls = cls;
	}
	
	
}
